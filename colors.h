#ifndef COLORS_H
#define COLORS_H

// Reset color and attributes
#define RESET   "\x1b[0m"

// Foreground colors
#define BLACK   "\x1b[30m"
#define RED     "\x1b[31m"
#define GREEN   "\x1b[32m"
#define YELLOW  "\x1b[33m"
#define BLUE    "\x1b[34m"
#define MAGENTA "\x1b[35m"
#define CYAN    "\x1b[36m"
#define WHITE   "\x1b[37m"

// Text attributes
#define BOLD    "\x1b[1m"
#define UNDERLINE "\x1b[4m"
#define INVERT  "\x1b[7m"

#endif
